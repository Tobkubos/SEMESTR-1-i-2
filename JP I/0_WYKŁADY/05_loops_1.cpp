#include <iostream>
using namespace std;

//======================================================================

int main(int argc, char *argv[])
{
   int i, n=2;

//--- Cykl  for --------------------------------------------------------
   cout << "\nCykl for\n";

   for(i=1; i<=n; i++)
   {
      cout << i << endl;
   }

//--- Cykl  while ------------------------------------------------------
   cout << "\nCykl while\n";

   i = 1;
   while( i<=n )
   {
      cout << i << endl;
      i++;
   }

//--- Cykl  do-while ---------------------------------------------------
   cout << "\nCykl do - while\n";

   i = 1;
   do
   {
      cout << i << endl;
      i++;
   } while( i<=n );

//----------------------------------------------------------------------
//    system("PAUSE");
    return 0;
}
